function add(a, b) {
  return a + b
}

function subtract(a, b) {
  return a - b
}

function multiply(a, b) {
  return a * b
}

function divide(a, b) {
  return a / b
}

function increment(a) {
  return ++a
}

function decrement(b) {
  return --b
}

function makeInt(string) {
  return parseInt(string, 10)
}

function preserveDecimal(n) {
  return parseFloat(n)
}
